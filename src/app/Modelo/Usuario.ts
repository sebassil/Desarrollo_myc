export class Usuario{
    idUsuario: Number;
    nombre:String;
    puntos:number;
    correo:String;
    clave:String;
    fechaNacimiento:Date;
    estado:Number;
    administrador:boolean;

    constructor(){
        this.idUsuario=this.idUsuario;
        this.nombre=this.nombre;
        this.puntos=this.puntos;
        this.correo=this.correo;
        this.clave=this.clave;
        this.fechaNacimiento=this.fechaNacimiento;
        this.estado=this.estado;
        this.administrador=this.administrador;
    }
}